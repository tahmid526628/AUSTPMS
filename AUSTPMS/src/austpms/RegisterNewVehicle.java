    private void numFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numFieldKeyTyped
        // TODO add your handling code here:
        char ch = evt.getKeyChar();
        if(charCountForVehicleNum < 6){
        if(!Character.isDigit(ch)){
            evt.consume();
            
            if(ch == evt.VK_BACK_SPACE || ch == evt.VK_DELETE){
                charCountForVehicleNum--;
                if(charCountForVehicleNum < 0){
                    charCountForVehicleNum = 0;
                }
            }
            else
                getToolkit().beep();
        }
        else{
            charCountForVehicleNum++;
            System.out.println(charCountForVehicleNum);
        }
        }
        else{
            evt.consume();
            if(ch == evt.VK_BACK_SPACE || ch == evt.VK_DELETE){
                charCountForVehicleNum--;
                if(charCountForVehicleNum < 0){
                    charCountForVehicleNum = 0;
                }
            }
            else
                getToolkit().beep();
        }
    }//GEN-LAST:event_numFieldKeyTyped

