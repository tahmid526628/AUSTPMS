    private void expDateFieldMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_expDateFieldMouseClicked
        // TODO add your handling code here:
        
        if(profileActiveState){
            String profession[] = professionLabel.getText().split(" ", 2);
            if(regDateField.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please click on Registration Date field first");
            }
            else{
                String curDate = regDateField.getText();
                String splitDate[] = curDate.split("-", 3);

                int date = Integer.parseInt(splitDate[2]);
                int month = Integer.parseInt(splitDate[1]);
                int year = Integer.parseInt(splitDate[0]);
                int expireIn;
                int extendMonth = 0;
                int extendYear = 0;
                if(profession[1].equals("Student")){
                    expireIn = 6;
                    extendMonth = month + expireIn;

                    if(extendMonth <= 12){
                        expDateField.setText(String.valueOf(year) + "-" + String.valueOf(extendMonth) + "-" + String.valueOf(date));
                    }
                    else{
                        year = year + 1;
                        extendMonth = extendMonth % 12;
                        expDateField.setText(String.valueOf(year) + "-" + String.valueOf(extendMonth) + "-" + String.valueOf(date));
                    }
                }
                else if(profession[1].equals("Teacher")|| profession.equals("Staff")){
                    expireIn = 1;
                    extendYear = year + expireIn;

                    expDateField.setText(String.valueOf(extendYear) + "-" + String.valueOf(month) + "-" + String.valueOf(date));
                }
                else{
                    JOptionPane.showMessageDialog(null, "Please select profession");
                }
            }
        }
    }//GEN-LAST:event_expDateFieldMouseClicked

    private void idSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idSubmitActionPerformed
        // TODO add your handling code here:
        String id = null;
        
        if(idField.getText().equals("") || idField.getText().equals("Please enter your ID")){
            JOptionPane.showMessageDialog(null, "Please enter your ID first");
            profileActiveState = false;
        }
        else{
            
//            name = "Tahmid Khandokar";
//            
//            profession = "Teacher";
//            department = "CSE";
//            vehicle = 1;

            id = idField.getText();
            
            ConnectDatabase connectDatabase = new ConnectDatabase();
            connectDatabase.ConnectDB();
            
            connectDatabase.retrieveDataNewVehicle(id);
            
            if(connectDatabase.getId() == null){
                JOptionPane.showMessageDialog(null, "Data not found\nPlease check your ID");
            }else{
            profession = connectDatabase.getProfession();
            nameLabel.setText(connectDatabase.getFirstName() + " " + connectDatabase.getLastName());
            idLabel.setText("ID: " + connectDatabase.getId());
            professionLabel.setText("Profession: " + connectDatabase.getProfession());
            deptLabel.setText("Department: " + connectDatabase.getDepartment());
            vehicleLabel.setText("Number of Vehicle: " + connectDatabase.getCountVehicle());
            
            
            
            /******************set image(covert byte array to ImageIcon*************/
            try{
                byte[] pic = connectDatabase.getPhoto();        
                ImageIcon img = new ImageIcon(pic);
                Image image = img.getImage();
                Image myImg;
                myImg = image.getScaledInstance(imagePanel.getWidth(), imagePanel.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImg = new ImageIcon(myImg);
                imageLabel.setText("");
                imageLabel.setIcon(newImg);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Image not found");
            }
                       

            enableField(true);
            profileActiveState = true;
            }
        }
    }//GEN-LAST:event_idSubmitActionPerformed

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:         
        String vehicleType = typeCombo.getSelectedItem().toString();
        String vehicleModel = modelField.getText();
        String vehicleColor = colorField.getText();
        
        String vehicleSerial = serialCombo.getSelectedItem().toString();
        String vehicleNum = numField.getText();
        
        String regDate = regDateField.getText();
        String expDate = expDateField.getText();
        
        
        if(profileActiveState){
            ConnectDatabase connectDatabase = new ConnectDatabase();
            connectDatabase.ConnectDB();  
            
            connectDatabase.storeVehicleData(vehicleSerial, vehicleNum, vehicleType, vehicleModel, vehicleColor, regDate, expDate, idField.getText());
            JOptionPane.showMessageDialog(null, "Data for " + idField.getText() + " is updated");
        }
    }//GEN-LAST:event_submitButtonActionPerformed

